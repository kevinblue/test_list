public enum MeassageNoticeType {
	
    PERSONAL("PERSONAL","个人消息"),
	PUBLIC  ("PUBLIC"  ,"公共消息");
    
	private final String name;
	private final String value;
	
	private MeassageNoticeType(String name, String value) {
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}
}
